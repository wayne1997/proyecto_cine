    private void llenarTabla(){
        modelo = new DefaultTableModel();
        modelo.addColumn("Cedula");
        modelo.addColumn("Nombre");
        modelo.addColumn("Correo");
        modelo.addColumn("Direccion");
        tblClientes.setModel(modelo);
    }
    
    private void listarClientes(){
        llenarTabla();
        cliManager = new ClienteManager();
        lstClientes = cliManager.obtenerClientes();
        lstClientes.forEach(cliente->{
            Object[] dato = new Object[4];
            dato[0] = cliente.getCedula();
            dato[1] = cliente.getNombres();
            dato[2] = cliente.getCorreoElectronico();
            dato[3] = cliente.getDireccion();
            modelo.addRow(dato);
        });
    }

