    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        try {
            cliente = new Cliente();
            cliManager = new ClienteManager();
            cliente.setCedula(txtCI.getText());
            cliente.setNombres(txtNombre.getText());
            cliente.setApellidos(txtApellido.getText());
            cliente.setDireccion(txtDireccion.getText());
            cliente.setCorreoElectronico(txtCorreo.getText());
            cliente.setClave(txtPassword.getText());
            cliManager.insertarCliente(cliente);
            vaciarCampos();
            emitirMensaje("Datos guardados con exito");
            listarClientes();
        } catch (Exception ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
            emitirMensaje(ex.getMessage());
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        cliente = lstClientes.get(tblClientes.getSelectedRow());
        txtNombre.setText(cliente.getNombres());
        txtApellido.setText(cliente.getApellidos());
        txtCI.setText(cliente.getCedula());
        txtCorreo.setText(cliente.getCorreoElectronico());
        txtDireccion.setText(cliente.getDireccion());
    }//GEN-LAST:event_btnSeleccionarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        cliManager = new ClienteManager();
        
        cliente = new Cliente();
            cliManager = new ClienteManager();
            cliente.setCedula(txtCI.getText());
            cliente.setNombres(txtNombre.getText());
            cliente.setApellidos(txtApellido.getText());
            cliente.setDireccion(txtDireccion.getText());
            cliente.setCorreoElectronico(txtCorreo.getText());
            cliente.setClave(txtPassword.getText());
        try {
            cliManager.actualizarCliente(cliente);
            emitirMensaje("Datos actualizados con exito");
            vaciarCampos();
            listarClientes();
        } catch (Exception ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
            emitirMensaje(ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            cliente = lstClientes.get(tblClientes.getSelectedRow());
            cliManager = new ClienteManager();
            cliManager.eliminarCliente(cliente.getCedula());
            emitirMensaje("Dato eliminado correctamente");
            listarClientes();
        } catch (Exception ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
            emitirMensaje(ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void emitirMensaje(String mensaje){
        JOptionPane.showMessageDialog(null, mensaje);
    }
    private void vaciarCampos(){
        txtNombre.setText("");
        txtApellido.setText("");
        txtCorreo.setText("");
        txtPassword.setText("");
        txtCI.setText("");
        txtDireccion.setText("");
    }
